def build_tasklist(request: RenderRequest) -> list[dict]:
    package = "tasklist"
    env_var = "TASKLIST_STORE"
    command = "tasklist-cli"
    rendered = request.rendered_unit_ids
    files: list[dict] = [
        _m(".gitignore", _GITIGNORE),
        _m("pyproject.toml", _cli_pyproject("tasklist", package, command, ["taskstore"])),
        _m("README.md", _readme("tasklist", f"public command: {command}")),
        _m(
            f"src/{package}/__init__.py",
            '"""Generated task-list CLI (requires taskstore)."""\n',
        ),
        _m(f"src/{package}/cli.py", _tasklist_cli(package, env_var)),
        _provenance(request, package),
        _m("tests/conftest.py", _CONFTEST),
    ]
    if "FR1" in rendered:
        files.append(_m("tests/test_fr1.py", _tasklist_unit_fr1(package, env_var)))
    if "FR2" in rendered:
        files.append(_m("tests/test_fr2.py", _tasklist_unit_fr2(package, env_var)))

    files.append(_tasklist_conformance(request, package, env_var))
    return files


def _tasklist_cli(package: str, env_var: str) -> str:
    return f'''from __future__ import annotations

import argparse
import os
from pathlib import Path
from typing import Sequence

from taskstore import TaskStore


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(prog="{package}")
    subparsers = parser.add_subparsers(dest="command")

    add_parser = subparsers.add_parser("add")
    add_parser.add_argument("text")

    subparsers.add_parser("list")
    return parser


def store_from_env() -> TaskStore:
    path = Path(os.environ.get("{env_var}", "tasks.json"))
    return TaskStore(path)


def main(argv: Sequence[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    store = store_from_env()

    if args.command == "add":
        task = store.add_task(args.text)
        print(f"[ ] {{task.text}}")
        return 0

    if args.command == "list":
        for task in store.list_tasks():
            marker = "x" if task.completed else " "
            print(f"[{{marker}}] {{task.text}}")
        return 0

    parser.print_help()
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
'''


