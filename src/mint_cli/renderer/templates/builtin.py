def build_taskstore(request: RenderRequest) -> list[dict]:
    package = "taskstore"
    rendered = request.rendered_unit_ids
    files: list[dict] = [
        _m(".gitignore", _GITIGNORE),
        _m("pyproject.toml", _lib_pyproject("taskstore", package)),
        _m("README.md", _readme("taskstore", "library API: Task, TaskStore")),
        _m(
            f"src/{package}/__init__.py",
            f'"""Generated storage library."""\n\n'
            f"from .store import Task, TaskStore\n\n"
            f'__all__ = ["Task", "TaskStore"]\n',
        ),
        _m(f"src/{package}/store.py", _store_module(package)),
        _provenance(request, package),
        _m("tests/conftest.py", _CONFTEST),
    ]
    if "FR1" in rendered:
        files.append(_m("tests/test_fr1.py", _store_unit_fr1(package)))
    if "FR2" in rendered:
        files.append(_m("tests/test_fr2.py", _store_unit_fr2(package)))

    files.append(_taskstore_conformance(request, package))
    return files


