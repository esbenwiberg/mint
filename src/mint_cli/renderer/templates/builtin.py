def _taskstore_conformance(request: RenderRequest, package: str) -> dict:
    unit = request.current_unit_id
    if unit == "FR1":
        body = f'''from __future__ import annotations

from {package} import Task, TaskStore


def test_public_api_add_then_list(tmp_path):
    store = TaskStore(tmp_path / "tasks.json")
    store.add_task("Buy milk")

    tasks = TaskStore(tmp_path / "tasks.json").list_tasks()

    assert tasks == [Task(text="Buy milk", completed=False)]
'''
    elif unit == "FR2":
        body = f'''from __future__ import annotations

from {package} import TaskStore


def test_public_api_preserves_order(tmp_path):
    store = TaskStore(tmp_path / "tasks.json")
    store.add_task("Buy milk")
    store.add_task("Write notes")

    reloaded = TaskStore(tmp_path / "tasks.json")
    assert [t.text for t in reloaded.list_tasks()] == ["Buy milk", "Write notes"]
'''
    else:
        body = _generic_conformance(package)
    return _c(f"{unit}/test_{unit.lower()}.py", body)


