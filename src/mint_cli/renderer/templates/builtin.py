def _generic_conformance(package: str) -> str:
    return f'''from __future__ import annotations


def test_generated_package_imports():
    __import__("{package}")
'''


def _readme(module: str, blurb: str) -> str:
    return f"# {module}\n\nGenerated by `mint`.\n\n{blurb}.\n"


def _lib_pyproject(name: str, package: str) -> str:
    return f'''[build-system]
requires = ["setuptools>=68"]
build-backend = "setuptools.build_meta"

[project]
name = "{name}"
version = "0.1.0"
requires-python = ">=3.12"
dependencies = []

[tool.setuptools.packages.find]
where = ["src"]
'''


def _cli_pyproject(name: str, package: str, command: str, requires: list[str]) -> str:
    deps = ", ".join(f'"{req}"' for req in requires)
    return f'''[build-system]
requires = ["setuptools>=68"]
build-backend = "setuptools.build_meta"

[project]
name = "{name}"
version = "0.1.0"
requires-python = ">=3.12"
dependencies = [{deps}]

[project.scripts]
{command} = "{package}.cli:main"

[tool.setuptools.packages.find]
where = ["src"]
'''
