"""Built-in deterministic templates for the demo specs.

Three template sets:
  * ``taskstore`` — a storage library (Task + TaskStore), no CLI.
  * ``tasklist``  — a CLI that ``requires`` taskstore and imports it at runtime.
  * ``example``   — the original self-contained task-list (store + CLI in one pkg).
"""

from __future__ import annotations

import json

from ..base import RenderRequest


def _m(path: str, contents: str) -> dict:
    return {"path": path, "action": "write", "contents": contents, "root": "module"}


def _provenance(request: RenderRequest, package: str) -> dict:
    """A generated provenance file that ties emitted code to the spec it came from.

    Because its contents derive from every unit rendered so far (id, title, spec and
    acceptance text), any meaningful spec change changes the generated code — which is
    what makes a required module's ``requiredModuleCodeHash`` move and cascade to
    dependents, mirroring how a real model renderer would emit different code.
    """
    payload = {
        "module": request.module,
        "stack": request.stack,
        "units": request.units_so_far,
    }
    body = json.dumps(payload, indent=2, sort_keys=True)
    return _m(
        f"src/{package}/_mint_provenance.py",
        '"""Generated by mint. Provenance of the spec this code was rendered from."""\n\n'
        f"PROVENANCE = {body}\n",
    )


def _c(path: str, contents: str) -> dict:
    return {"path": path, "action": "write", "contents": contents, "root": "conformance"}


# A conftest that makes the module's own src importable and also honours
# MINT_REQUIRED_SRC so generated code can import its required modules at test time.
_CONFTEST = '''from __future__ import annotations

import os
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "src"))

for extra in os.environ.get("MINT_REQUIRED_SRC", "").split(os.pathsep):
    if extra:
        sys.path.insert(0, extra)
'''

_GITIGNORE = '''__pycache__/
*.py[cod]
.pytest_cache/
tasks.json
'''


def _store_module(package: str) -> str:
    return f'''from __future__ import annotations

from dataclasses import dataclass
import json
from pathlib import Path


@dataclass(frozen=True)
class Task:
    text: str
    completed: bool = False


class TaskStore:
    """File-backed list of tasks for {package}."""

    def __init__(self, path: str | Path) -> None:
        self.path = Path(path)

    def add_task(self, text: str) -> Task:
        task = Task(text=text, completed=False)
        tasks = self.list_tasks()
        tasks.append(task)
        self._save(tasks)
        return task

    def list_tasks(self) -> list[Task]:
        if not self.path.exists():
            return []
        data = json.loads(self.path.read_text(encoding="utf-8"))
        return [Task(text=item["text"], completed=bool(item["completed"])) for item in data]

    def _save(self, tasks: list[Task]) -> None:
        self.path.parent.mkdir(parents=True, exist_ok=True)
        self.path.write_text(
            json.dumps(
                [{{"text": task.text, "completed": task.completed}} for task in tasks],
                indent=2,
            ),
            encoding="utf-8",
        )
'''


