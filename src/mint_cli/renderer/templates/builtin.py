    add = run_cli(tmp_path, "add", "Buy milk")
    assert add.returncode == 0, add.stderr

    listed = run_cli(tmp_path, "list")
    assert listed.returncode == 0, listed.stderr
    assert "[ ] Buy milk" in listed.stdout
