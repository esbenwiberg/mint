    assert run_cli(tmp_path, "add", "Buy milk").returncode == 0
    assert run_cli(tmp_path, "add", "Write notes").returncode == 0

    listed = run_cli(tmp_path, "list")
    assert listed.returncode == 0, listed.stderr
    assert listed.stdout.index("Buy milk") < listed.stdout.index("Write notes")
